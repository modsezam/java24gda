public class FahrenheitConverter {

    public static void main(String[] args) {


        System.out.println(">>> Task 7");

        double fahrenheit1 = 212;
        double celciusz1 = (fahrenheit1 - 32) / 1.8;

        System.out.print(fahrenheit1 + " st F = (st C) ");
        System.out.println(celciusz1);



        double fahrenheit2 = 32;
        double celciusz2 = (fahrenheit2 - 32) / 1.8;

        System.out.print(fahrenheit2 + " st F = (st C) ");
        System.out.println(celciusz2);

    }




}
